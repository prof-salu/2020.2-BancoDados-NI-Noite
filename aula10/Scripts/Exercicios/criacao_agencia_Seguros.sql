-- DATABASE
CREATE DATABASE agencia_seguros;

-- TABELAS
CREATE TABLE modelo(
	cod_modelo INTEGER,
	nome VARCHAR(40) NOT NULL,
	PRIMARY KEY(cod_modelo)
);

CREATE TABLE PROPRIETARIO(
	cod_prop INTEGER,
	nome VARCHAR(40),
	cpf CHAR(11) UNIQUE NOT NULL,
	PRIMARY KEY(cod_prop)
);

CREATE TABLE VEICULO(
	placa CHAR(7),
	cor VARCHAR(20),
	modelo INTEGER REFERENCES modelo(cod_modelo),
	proprietario INTEGER REFERENCES proprietario(cod_prop),
	ano_fab CHAR(4),
	ano_mod CHAR(4),
	valor_segurado NUMERIC(9,2)
);

